package DataTypesAndVariables;

public class Arithmatic_Operations {
	public static void main(String[] args) {
		int a=10 ;
		int b=5;
		
		//Addition
		System.out.println(a+b);
		
		//substraction
		System.out.println(a-b);
		
		//Multiplication
		System.out.println(a*b);
		
		//Division
		
		System.out.println(a/b);

		//Remainder
		System.out.println(a%b);
		
	}

}
